void main() {
  int contador = 0;
  
  //exemplo de while
  while (contador != 10) {
    print("O valor do contador é $contador");
    contador++;
  }
  
  //exemplo com do while
  do {
    print("O valor do contador é $contador");
    contador++;
  } while (contador <= 5);

  print("Finalizando o programa");

}
